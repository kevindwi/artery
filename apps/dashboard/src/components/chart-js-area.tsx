export function ChartJsArea() {
  const [timeRange, setTimeRange] = React.useState("90d");

  const filteredData = rawData.filter((item) => {
    const date = new Date(item.date);
    const referenceDate = new Date("2024-06-30");
    let daysToSubtract = 90;
    if (timeRange === "30d") {
      daysToSubtract = 30;
    } else if (timeRange === "7d") {
      daysToSubtract = 7;
    }
    const startDate = new Date(referenceDate);
    startDate.setDate(startDate.getDate() - daysToSubtract);
    return date >= startDate;
  });

  const getChartColors = () => {
    const isDark = document.documentElement.classList.contains("dark");

    if (isDark) {
      return {
        mobile: {
          border: "oklch(70.5% 0.213 47.604)",
          gradientStart: "oklch(70.5% 0.213 47.604)",
          gradientEnd: "oklch(70.5% 0.213 47.604 / 5%)",
        },
        desktop: {
          border: "oklch(83.7% 0.128 66.29)",
          gradientStart: "oklch(83.7% 0.128 66.29)",
          gradientEnd: "oklch(83.7% 0.128 66.29 / 5%)",
        },
      };
    } else {
      return {
        mobile: {
          border: "oklch(70.5% 0.213 47.604)",
          gradientStart: "oklch(70.5% 0.213 47.604)",
          gradientEnd: "oklch(70.5% 0.213 47.604 / 5%)",
        },
        desktop: {
          border: "oklch(83.7% 0.128 66.29)",
          gradientStart: "oklch(83.7% 0.128 66.29)",
          gradientEnd: "oklch(83.7% 0.128 66.29 / 5%)",
        },
      };
    }
  };

  const colors = getChartColors();

  const chartDataConfig = {
    labels: filteredData.map((item) => {
      const date = new Date(item.date);
      return date.toLocaleDateString("en-US", {
        month: "short",
        day: "numeric",
      });
    }),
    datasets: [
      {
        label: "Mobile",
        data: filteredData.map((item) => item.mobile),
        borderColor: colors.mobile.border,
        backgroundColor: (context: any) => {
          const ctx = context.chart.ctx;
          const gradient = ctx.createLinearGradient(0, 0, 0, 210);
          gradient.addColorStop(0, colors.mobile.gradientStart);
          gradient.addColorStop(0.8, colors.mobile.gradientEnd);
          return gradient;
        },
        fill: true,
        pointRadius: 0,
        borderWidth: 1,
        tension: 0.4,
      },
      {
        label: "Desktop",
        data: filteredData.map((item) => item.desktop),
        borderColor: colors.desktop.border,
        backgroundColor: (context: any) => {
          const ctx = context.chart.ctx;
          const gradient = ctx.createLinearGradient(0, 0, 0, 210);
          gradient.addColorStop(0, colors.desktop.gradientStart);
          gradient.addColorStop(0.8, colors.desktop.gradientEnd);
          return gradient;
        },
        fill: true,
        pointRadius: 0,
        borderWidth: 1,
        tension: 0.4,
      },
    ],
  };

  const options = {
    responsive: true,
    maintainAspectRatio: false,

    plugins: {
      legend: {
        display: true,
        position: "bottom" as const,
        align: "center",
        labels: {
          boxWidth: 20,
          padding: 20,
        },
      },
      tooltip: {
        callbacks: {
          label: function (context: any) {
            return context.dataset.label + ": " + context.parsed.y;
          },
        },
      },
    },
    scales: {
      x: {
        grid: {
          display: false,
        },
        ticks: {
          maxTicksLimit: 15,
          autoSkip: true,
        },
      },
      y: {
        beginAtZero: true,
        stacked: true,
        border: {
          display: false,
        },
        ticks: {
          display: false,
        },
        grid: {
          display: false,
        },
      },
    },
    interaction: {
      mode: "index",
      intersect: false,
    },
  };

  return (
    <Card className="pt-0">
      <CardHeader className="flex items-center gap-2 space-y-0 border-b py-5 sm:flex-row">
        <div className="grid flex-1 gap-1">
          <CardTitle>Area Chart - Interactive</CardTitle>
          <CardDescription>
            Showing total visitors for the last 3 months
          </CardDescription>
        </div>
        <Select value={timeRange} onValueChange={setTimeRange}>
          <SelectTrigger
            className="hidden w-[160px] sm:flex"
            aria-label="Select a value"
          >
            <SelectValue placeholder="Last 3 months" />
          </SelectTrigger>
          <SelectContent>
            <SelectItem value="90d">Last 3 months</SelectItem>
            <SelectItem value="30d">Last 30 days</SelectItem>
            <SelectItem value="7d">Last 7 days</SelectItem>
          </SelectContent>
        </Select>
      </CardHeader>
      <CardContent className="px-2 pt-4 sm:px-6 sm:pt-6">
        <div className="aspect-auto h-[250px] w-full">
          <Line data={chartDataConfig} options={options} />
        </div>
      </CardContent>
    </Card>
  );
}
